# Programa: ej9sec.py
# Propósito: Una tienda ofrece un descuento del 15% sobre el total de la compra y un cliente desea saber cuanto
# deberá pagar finalmente por su compra.
# Autor: Antonio Quesada
# Fecha: 13/10/2019.
#
# Variables a usar:
#   * precio
#   * descuento
#
# Algoritmo:
#   LEER precio
#   descuento <-- (precio-precio*0.15)
#   ESCRIBIR descuento


# Petición de datos
precio = float(input("Inserte el precio de la compra: "))
print("-----------------------------------------------------------")

#Cálculos
descuento = (precio-precio*0.15)

#Cálculos y Salida
print ("El descuento del total de la compra es", descuento, "euros")





	
	

	






	
	

	
