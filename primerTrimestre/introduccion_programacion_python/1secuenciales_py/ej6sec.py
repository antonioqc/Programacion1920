# Algoritmo:
#   LEER num1, num2, num3
#   media <-- (num1+num2+num3)/3
#   ESCRIBIR media


# Petición de datos
num1 = float(input("Inserte el primer número: "))
num2 = float(input("Inserte el segundo número: "))
num3 = float(input("Inserte el tercer número: "))
print("-------------------------------------------")

#Cálculos
media = (num1+num2+num3)/3

# Salida
print("La media de los tres números es ", media)




	
	

	
