# Propósito: Pide al usuario dos números y muestra la "distancia" entre ellos (el valor absoluto de su diferencia,
# de modo que el resultado sea siempre positivo).
#
# Autor: Antonio Quesada
# Fecha: 13/10/2019.
#
# Variables a usar:
#   * num1
#   * num2
#   * distancia
#
# Algoritmo:
#   LEER num1, num2
#   distancia <-- |num2-num1|
#   ESCRIBIR distancia


# Petición de datos
num1 = float(input("Inserte un número: "))
num2 = float(input("Inserte otro número: "))
print("---------------------------------------------------------------")

#Cálculos
distancia = abs(num2-num1)

#Cálculos y Salida
print ("La distancia entre ambos números es", distancia)





	
	

	






	
	

	
